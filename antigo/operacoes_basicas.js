// concatenação
const primeiroNome = 'Gabriel'
const sobrenome = 'Rodrigues'

const nomeCompleto = primeiroNome + ' ' + sobrenome
console.log(primeiroNome)
console.log(sobrenome)
console.log(nomeCompleto)

// somar
const primeiroValor = 11
const segundoValor = 11.87

const soma = primeiroValor + segundoValor

console.log('Soma: ' + soma)

// subtração
const subtracao = primeiroValor - segundoValor

console.log(subtracao)

// multiplicação
const multiplicacao = primeiroValor * segundoValor

console.log(multiplicacao)

// divisão
const divisao = primeiroValor / segundoValor

console.log(divisao)

// comparação
const primeiroNumero = '11'
const segundoNumero = 11

// true pois == compara somente o valor
console.log(primeiroNumero == segundoNumero) 

// false pois === compara o valor e o tipo
console.log(primeiroNumero === segundoNumero)

// operador de modulo -> resto da divisão
console.log(10 % 2)